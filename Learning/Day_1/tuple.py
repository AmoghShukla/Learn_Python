# Tuples in Python
# Tuples are ordered collections of items that can be of different types.
# They are immutable, meaning their contents cannot be changed after creation.

my_tuple = (1, 2, 3, 'four', 'five')
print(my_tuple)

# Define a single value tuple
a = (5,)
print(type(a))  # Output: <class 'tuple'>